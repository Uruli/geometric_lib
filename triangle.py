def area(a, b, c):
    '''принимает числа a,b,c, возвращает сумму чисел делённую на два'''
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''Описание:
Вычисляет периметр треугольника по сторонам a, b, c.

    Параметры:
a, b, c (float или int): Длины сторон треугольника.

    Возвращаемое значение:
(float или int): Периметр треугольника.'''
    return a + b + c
